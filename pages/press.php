<h2>For-sale.ie <br>the project of 2 young geeks from Belgium.</h2>
    <br>
    <h3>All the way back in 2000, Bart and Vincent, the founders of For-sale.ie, are already getting involved in the internet in Belgium.</h3>
    <br>
    <p>
      Computer literate almost before the term existed, Bart soon gets noticed by eBay who hire him within the context of their European deployment. Everything is still to be developed on the web and so Bart forges a solid expertise in all aspects of e-commerce: technology, of course, but also in specific marketing for this sector.
    </p>
    <p>
    He rises through the ranks during the 6 years he spends at eBay but secretly nurtures a dream of going independent…
    </p>
    <p>
    Vincent is a pioneer in web strategy. SEO, audience acquisition and ROI are daily fare for him long before the advent of social networks. Consultant and strategist, he guides a large number of companies into the digital era and anticipates the developments that make the web what it is today. 
    </p>
    <p>
    After seven years of consulting, Vincent has become a recognised expert in digital strategy and wishes to apply this expertise within the context of major internet retail brands that are moving into Europe and gaining market shares.
    </p>
    <p>
    It is in 2010 that Bart and Vincent meet, on the largest private sales site in the Benelux. For both men, this is a year of new challenges for a system of selling that is expanding exponentially.
    </p>
    <p>
    Their vision of the market is clear: the global crisis has led to users seeking more and better bargains. But while the volume of classified ads is already impressive (500,000/day), the issue lies in the difficulty that some users have in navigating the plethora of ads.
    </p>
    <p>
    <span class="b">With this in mind, they came up with the concept of a user-friendly “meta site” devoted to second-hand and vintage products that would contain a powerful search engine and advanced features. </span>
    They challenged themselves – backed by their respective professional experience and knowledge of handling large databases – to create a website that would enable individuals to find the best deals from among the 50 million classified ads placed online in the UK.
    </p>
    <p class="b">How to do it? By creating advanced algorithms and filters that allow personalised databases to be created according to user search profiles. You had to be a geek to think that up!
    </p>
    <p>
    <span class="b">2011 sees the launch of the beta for the first portal devoted to the second hand market.<br></span>
    For-sale.ie begins as a start-up with the help of family and friends who all believe in the concept. This is an IT success story that began in an attic rather than in a garage… 
    <br>
   The available resources are pushed to the limit, the two partners using all their spare time to optimise the site, installing new servers under the eaves, dealing with technical problems and so forth, all while checking visitor statistics and trading volumes on For-sale.ie, which mount to a crescendo.
    </p>
    <p>
    <span class="b">A concept in continual evolution </span>with the objective to better respond to the needs of users in search of second-hand products.<br>
    Optimisations and innovations succeed on one another as time goes on, bringing together an active and loyal community that now counts almost 1.5 million visitors per month in the UK alone.
    </p>
    <p>
    Great results have motivated Bart and Vincent to keep up the momentum with regular additions of new services such as the recent mobile application For-sale.ie.
    </p>
    
    <p class="b">For-sale.ie is the fruit of a professional collaboration but it is also the result of a fusion of common interests between two real friends who are passionate about applying new technologies to the needs of ordinary people.</p>
    <p class="b">This is about the true spirit of knowledge sharing – in sharp contrast to the practices of the web industry today – and about putting the user at the heart of a project.</p>
